// 取得指定語言的景點資料
function getLocalizedSpot(spotId, lang = 'zh-TW') {
    // 如果是中文,直接返回原始資料
    if (lang === 'zh-TW') {
        return spotsData.find(spot => spot.id === spotId);
    }
    
    // 否則合併翻譯
    const originalSpot = spotsData.find(spot => spot.id === spotId);
    const translation = spotsI18n[lang] && spotsI18n[lang][spotId];
    
    if (originalSpot && translation) {
        return {
            ...originalSpot,
            name: translation.name,
            shortDesc: translation.shortDesc,
            fullDescription: translation.fullDescription,
            tips: translation.tips || originalSpot.tips
        };
    }
    
    return originalSpot;
}

// 取得所有景點的多語言版本
function getAllLocalizedSpots(lang = 'zh-TW') {
    return spotsData.map(spot => getLocalizedSpot(spot.id, lang));
}
